void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	static uint8_t first = 1;
	if (first)
	{
        g_wearable_state = STANDARD_MODE;
        first = 0;
        //osSemaphoreRelease(myBinarySem01Handle);
        xSemaphoreGiveFromISR(myBinarySem01Handle, &xHigherPriorityTaskWoken);
        portYIELD_FROM_ISR( xHigherPriorityTaskWoken );
        return;
	}

	g_wearable_state = (g_wearable_state == STANDARD_MODE) ? RAW_DATA_MODE : STANDARD_MODE;
	portYIELD_FROM_ISR( xHigherPriorityTaskWoken );
}

void compute_step_count_crude(int *step_count, int16_t *accel_data)
{
  char debug_buf[100];
  static int count = 0;
  static uint8_t threshold_set = 0;
  static int16_t accelMagnitude[10];
  int16_t current;
  static int16_t accelAvg;
  int16_t threshold = 150;


  accelMagnitude[count] = sqrt(accel_data[0]*accel_data[0] + accel_data[1]*accel_data[1]+ accel_data[2]*accel_data[2]);
  current = accelMagnitude[count];

  ++count;
  if (count == 9)
  {
    int i;
    int16_t sum = 0;
    for (i = 0; i < 10; ++i)
    {
       sum += (int16_t)accelMagnitude[i];
    }
    accelAvg = sum/10;
    count = 0;
    threshold_set = 1;
  }

  snprintf(debug_buf, sizeof(debug_buf), "current: %d threshold %d accelAvg %d\r\n", current, threshold, accelAvg);
  logMsg(&huart1, debug_buf);
  if (threshold_set)
  {
    if (current > (threshold + accelAvg))
    {
        *step_count = *step_count + 1;
    }
  }

}

