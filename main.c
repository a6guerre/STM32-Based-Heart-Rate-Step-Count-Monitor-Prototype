static void appendCharToString(char *str, char ch)
{
  int len = strlen(str);
  str[len] = ch;
  str[len + 1] = '\0';
}

static void formatStringForScreen(char *str, int value)
{
    // Spaces available in 11x18 font
	char buf[12];
	int  digit_count;
	int spaces_to_add;

	char append_char = ' ';
    const int spaces_available = 11;
    int base_char_len = strlen(str);

    snprintf(buf, sizeof(buf), "%d", value);
    digit_count = strlen(buf);

    spaces_to_add = spaces_available - digit_count - base_char_len;
    // value will have to be truncated
    if (spaces_to_add == 0)
        return;

    for (int i = 0; i < spaces_to_add; ++i)
    {
        appendCharToString(str, append_char);
    }

    snprintf(buf, sizeof(buf), "%s%d", str, value);
    strcpy(str, buf);
}

